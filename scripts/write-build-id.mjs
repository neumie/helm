import { execFileSync } from 'node:child_process'
import { createHash } from 'node:crypto'
import { readFileSync, writeFileSync } from 'node:fs'
import { resolve } from 'node:path'

const root = resolve(import.meta.dirname, '..')
const runGit = args => execFileSync('git', args, { cwd: root, encoding: 'utf8' }).trim()

let head = 'development'
let dirty = ''
try {
	head = runGit(['rev-parse', '--short=12', 'HEAD'])
	const diff = execFileSync(
		'git',
		[
			'diff',
			'--binary',
			'HEAD',
			'--',
			'.',
			':(exclude)src/build-id.generated.ts',
			':(exclude)app/src/build-id.generated.ts',
		],
		{ cwd: root },
	)
	const hash = createHash('sha256').update(diff)
	const untracked = runGit(['ls-files', '--others', '--exclude-standard'])
	for (const path of untracked ? untracked.split('\n').sort() : []) {
		if (path === 'src/build-id.generated.ts' || path === 'app/src/build-id.generated.ts') continue
		hash.update(path)
		hash.update(readFileSync(resolve(root, path)))
	}
	if (diff.length > 0 || untracked) dirty = `-${hash.digest('hex').slice(0, 10)}`
} catch {
	// Source archives without .git still get a stable development identity.
}

const buildId = `${head}${dirty}`
const source = `// Generated by scripts/write-build-id.mjs — do not edit.\nexport const HELM_BUILD_ID = '${buildId}'\n`
writeFileSync(resolve(root, 'src/build-id.generated.ts'), source)
writeFileSync(resolve(root, 'app/src/build-id.generated.ts'), source)
process.stdout.write(`[helm] build id ${buildId}\n`)
